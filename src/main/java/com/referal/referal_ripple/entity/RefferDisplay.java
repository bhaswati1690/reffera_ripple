package com.referal.referal_ripple.entity;

public class RefferDisplay {
	private int totalReferrals;
    private int successfulReferrals;
    private int rewardPointsEarned;
	public int getTotalReferrals() {
		return totalReferrals;
	}
	public void setTotalReferrals(int totalReferrals) {
		this.totalReferrals = totalReferrals;
	}
	public int getSuccessfulReferrals() {
		return successfulReferrals;
	}
	public void setSuccessfulReferrals(int successfulReferrals) {
		this.successfulReferrals = successfulReferrals;
	}
	public int getRewardPointsEarned() {
		return rewardPointsEarned;
	}
	public void setRewardPointsEarned(int rewardPointsEarned) {
		this.rewardPointsEarned = rewardPointsEarned;
	}


}
